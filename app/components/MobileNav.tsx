// Short labels for bottom bar
const SHORT_LABEL: Record<string, string> = {
  '/actions':       'Actions',
  '/dashboard':     'Home',
  '/stock':         'Stock',
  '/purchase':      'Purchase',
  '/sales':         'Sales',
  '/notifications': 'Alerts',
  '/settings':      'Settings',
  '/reports':       'Reports',
  '/suppliers':     'Suppliers',
  '/users':         'Team',
};

export function MobileNav({ links, current }: { links: NavLink[]; current: string }) {
  // Show max 5 tabs — prioritise most useful
  const priority = ['/actions', '/sales', '/stock', '/dashboard', '/purchase', '/notifications', '/settings'];
  const sorted = [...links].sort((a, b) => priority.indexOf(a.href) - priority.indexOf(b.href));
  const visible = sorted.slice(0, 5);

  return (
    <nav className="mobile-bottom-nav">
      {visible.map((link) => {
        const isActive = current === link.href;
        return (
          <Link
            key={link.href}
            href={link.href}
            className={`mobile-nav-item${isActive ? ' active' : ''}`}
          >
            <span className="mobile-nav-icon">{MOBILE_ICONS[link.href]}</span>
            <span className="mobile-nav-label">{SHORT_LABEL[link.href] ?? link.label}</span>
          </Link>
        );
      })}
    </nav>
  );
}
